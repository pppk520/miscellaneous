class Solution:
    # @param A : tuple of integers
    # @param B : tuple of integers
    # @param C : tuple of integers
    # @return an integer
    def solve(self, A, B, C):
        friends = A
        capacities = B
        costs = C

        ret = 0
        max_target = max(friends)

        dp = [[0 for _ in range(len(costs))] for _ in range(max_target + 1)]

        for i in range(1, max_target + 1):
            for j in range(len(costs)):
                v1 = dp[i][j-1] if j > 0 else 99999999
                v2 = dp[i - capacities[j]][j] if (i >= capacities[j]) else 99999999

                dp[i][j] = min(v1, 
                               v2 + costs[j])


        ret = 0
        for target in friends:
            ret += dp[target][-1]

        return ret

