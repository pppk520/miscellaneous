class Solution:
    # @param A : list of integers
    # @return an integer
    def jump(self, A):
        count = 1
        start = 0
        end = A[0]

        if len(A) == 1:
            return 0

        while end > start:
            if end >= len(A) - 1:
                return count

            next_start = end + 1
            next_end = end
            for i in range(start, end + 1):
                next_end = max(next_end, i + A[i])

            end = next_end
            count += 1
            start = next_start

        return -1


